// Write a program that receives a number and prints on the screen 
// "Greater than 10" if the number is greater than 10. 
#include<iostream> 
using namespace std; 
int main() { 
    int x; 
    cout << "Enter a number: "; 
    cin >> x; 
    if (x > 10) { 
        cout << "Greater than 10!" << endl; 
    }
}
