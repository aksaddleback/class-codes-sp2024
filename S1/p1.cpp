#include<iostream>
using namespace std; 

int main() { 
    // we ask for a memory location in which we are going to store an integer
    // the location will be named x
    int x; 
    // we ask to write the value of 10 into the memory location called x.
    x = 10; 
    int y; 
    // we ask to write into the memory location called y the value of x*2 which is 20 
    y = x * 2; 
    cout << x << endl;
    // we ask to first print out y on the screen and then end-of-line to be printed out
    cout << y << endl; 
    // we see that all the c++ statements are ending with ; 
}